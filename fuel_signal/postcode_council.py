def primary_council(postcode: str) -> str | None:
    """Return the LGA name for a postcode, or None if not in the map."""
    return _PC_MAP.get(str(postcode))


def is_sydney_metro(postcode: str) -> bool:
    """Return True if the postcode falls within the Greater Sydney metro area."""
    council = primary_council(postcode)
    return council is not None and council in SYDNEY_METRO_COUNCILS


# Derived set of postcodes that map to a Sydney metro council.
# Use this for load-time postcode filtering; it avoids importing the full map
# everywhere the postcode set is needed.
SYDNEY_METRO_POSTCODES: frozenset[str] = frozenset(
    pc for pc in _PC_MAP if is_sydney_metro(pc)
)
